using Infrastructure.Context;
using Microsoft.EntityFrameworkCore;

namespace TradeQuickWinsAPI
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            #region Additional Injections
            // Connection string to database
            builder.Services.AddDbContext<RepositoryContext>(x =>
                x.UseSqlServer(builder.Configuration.GetConnectionString("TradeQuickWinsDBConnection")));


            #endregion Additional Injections

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}